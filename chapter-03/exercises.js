// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(a, b) {
  if (a < b) {
    return a;

  }  if (a > b) {
    return b;
    
  } else {
    return 0;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
  // base
  if (n === 0)  return true;      
  if (n === 1)  return false;    

  // recursion
  if (n < 0) return isEven(-n);  

  return isEven(n - 2);   
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  let count = 0;

  for (let i = 0; i < string.length; i++) {
    if (string[i] === char) {
      count++;
    }
  }

  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let count = 0;

  for (let i = 0; i < string.length; i++) {
    if (string[i].toUpperCase() === "B") {
      count++;
    }
  }

  return count;
}

